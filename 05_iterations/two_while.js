// While loop
let index = 0;
while (index < 10) {
    console.log(`Index is ${index}`);
    index += 3;
}

let myArray = ["Dinesh", "Ramesh", "Suresh", "Rajesh"];
index = 0;
while (index < myArray.length) {
    console.log(`Name is ${myArray[index]}`);
    index++;
}


// do while loop

